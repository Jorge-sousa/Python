# sets são tipos de dados no python que são mutáveis, 
# porém só aceitam tipos imutáveis como valor interno,
#sets são parecidos com dicts porém não tem chaves, somente valores

#creating an empty set

set_one = set() #empty set
set_two = {''}  #empty set

#creating a set with items
set_three = {'Jorge', 1, 2, 3}  #set with data
print(set_one, type(set_one))
print(set_two, type(set_two))
print(set_three, type(set_three))